The pandemic has made its way into our young lives and wrecked havoc in its wake.
<br>Due to unfortunate circumstances, we are obliged to follow measures to prevent
<br>the spread of it. The pandemic is indeed tiresome, but through it, we were given
<br>the time to reflect and see the light behind its shadows and realize things our
<br>young minds couldn’t comprehend before. Through quarantine we discovered ourselves,
<br>we realized things we didn’t think about before, and appreciate the blessings
<br>we received.
<br>
<br>A coronavirus is a kind of common virus that causes an infection in your nose,
<br>sinuses, or upper throat. Most coronaviruses aren't dangerous. In early 2020,
<br>after a December 2019 outbreak in China, the World Health Organizationidentified
<br>SARS-CoV-2 as a new type of coronavirus.
<br>The outbreak quickly spread around the world.
<br>
<br>COVID-19 is a disease caused by SARS-CoV-2 that can trigger what doctors call
<br>a respiratory tract infection. It can affect your upper respiratory tract
<br>(sinuses, nose, and throat) or lower respiratory tract (windpipe and lungs).
<br>
<br>It spreads the same way other coronaviruses do, mainly through person-to-person contact.
<br>Infections range from mild to deadly. SARS-CoV-2 is one of seven types of coronavirus, including
<br>the ones that cause severe diseases like Middle East respiratory syndrome (MERS) and sudden acute
<br>respiratory syndrome (SARS). The other coronaviruses cause most of the colds that affect us during
<br>the year but aren’t a serious threat for otherwise healthy people.
<br>
<br>These are unprecedented times. We need to work extra hard to manage our emotions well.
<br>Expect to have a lot of mixed feelings. Naturally we feel anxiety, and maybe waves of panic,
<br>particularly when seeing new headlines.
